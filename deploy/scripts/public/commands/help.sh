cmd_help() {
    echo ""
    echo "${BOLD}Fleet Management${RESET} v${FM_SCRIPT_VERSION}"
    echo ""
    echo "${BOLD}Usage:${RESET} ./deploy/deploy-public.sh <command>"
    echo ""
    echo "${BOLD}Commands:${RESET}"
    echo "  ${CYAN}up${RESET}                               Start Fleet Management (idempotent: bootstrap or restart)"
    echo "  ${CYAN}up --mdns${RESET}                        Start with mDNS device discovery"
    echo "  ${CYAN}up --logging${RESET}                     Start with Dozzle log viewer (port ${DOZZLE_PORT:-9999})"
    echo "  ${CYAN}up --env dev${RESET}                     Local dev mode (admin/admin, no Zitadel/SSL, ~60s)"
    echo "  ${CYAN}up --env local${RESET}                   Full local Docker stack with Zitadel"
    echo "  ${CYAN}up --env cloud-test${RESET}              CI cloud-test public-path stack"
    echo "  ${CYAN}up --ssl --domain fm.example.com${RESET} Start with HTTPS (Let's Encrypt)"
    echo "  ${CYAN}up --ssl selfsigned${RESET}              Start with HTTPS (self-signed cert)"
    echo "  ${CYAN}up --ssl custom --domain fm.example.com --cert /path/fullchain.pem --key /path/privkey.pem${RESET}"
    echo "                                     Start with HTTPS (custom certificate)"
    echo "  ${CYAN}upgrade${RESET}                          Pull newer images, then restart (same as: pull + up)"
    echo "  ${CYAN}upgrade --migrate-first --yes${RESET}     Run required DB/Zitadel migration before image upgrade"
    echo "  ${CYAN}upgrade --soak <sec>${RESET}             Extend post-health soak window; auto-rolls back on degradation"
    echo "  ${CYAN}migrate --plan-only${RESET}              Print DB/Zitadel/FM migration plan + inventory/report paths"
    echo "  ${CYAN}migrate --yes${RESET}                    Execute required staged DB/Zitadel/FM migration with rollback"
    echo "  ${CYAN}migrate --verify-restore auto|strict|off${RESET}"
    echo "                                   Control backup restore drills before destructive migration steps"
    echo "  ${CYAN}rollback${RESET}                         Revert FM to fleet-manager:rollback (tagged by previous upgrade)"
    echo "  ${CYAN}down${RESET}                             Stop Fleet Management and keep data"
    echo "  ${CYAN}down --volumes${RESET}                   Stop Fleet Management and delete all data (asks for confirmation)"
    echo "  ${CYAN}down --volumes --yes${RESET}             Same as above, skip confirmation (for scripting/CI)"
    echo "  ${CYAN}status${RESET}                           Show service status and health"
    echo "  ${CYAN}logs${RESET} [service]                   Show logs (follow mode)"
    echo "  ${CYAN}ip${RESET}                               Show access URLs"
    echo "  ${CYAN}doctor${RESET} [--ssl ...]               Troubleshoot readiness, dependencies, and SSL configuration"
    echo "  ${CYAN}rotate-secrets${RESET} [--all]           Rotate JWT, FM encryption key, and/or postgres password"
    echo "  ${CYAN}backup-state${RESET} (--age-recipient FILE|--passphrase)"
    echo "                                   Encrypted tarball of deploy/state/ (creds, keys, certs)"
    echo "  ${CYAN}backup-db${RESET} <list|inspect|verify|create|restore>"
    echo "                                   Fleet DB backups with manifests and checksum verification"
    echo "  ${CYAN}upgrade-audit${RESET} <snapshot|snapshot-zitadel|compare|zitadel-plan>"
