    echo "${BOLD}Environment overrides:${RESET}"
    echo "  FM_VERSION=${FM_VERSION}  FLEET_MANAGER_PORT=${FLEET_MANAGER_PORT}  ZITADEL_EXTERNALPORT=${ZITADEL_EXTERNALPORT}"
